#!/usr/bin/env node
'use strict';

const fs = require('node:fs');
const path = require('node:path');

const ROOT = path.resolve(__dirname, '..');
const SOURCE_PATH = path.join(ROOT, 'src', 'mcp', 'server-core-entry.js');
const OUTPUT_PATH = path.join(ROOT, 'src', 'mcp', 'maestro-server-core.js');
const STATUS_PREFIX = /^\[(CREATE|UPDATE|UNCHANGED)\] /;

function buildCoreModule() {
  const source = fs.readFileSync(SOURCE_PATH, 'utf8');

  return `// Generated by scripts/build-mcp-core.js — do not edit.
// Source entry: src/mcp/server-core-entry.js

${source}`;
}

function getStatus(current, next) {
  if (current == null) {
    return 'CREATE';
  }

  return current === next ? 'UNCHANGED' : 'UPDATE';
}

function main() {
  const checkMode = process.argv.includes('--check');
  const next = buildCoreModule();
  const current = fs.existsSync(OUTPUT_PATH) ? fs.readFileSync(OUTPUT_PATH, 'utf8') : null;
  const status = getStatus(current, next);
  const relativeOutputPath = path.relative(ROOT, OUTPUT_PATH);

  console.log(`[${status}] ${relativeOutputPath}`);

  if (checkMode) {
    if (status !== 'UNCHANGED') {
      process.exit(1);
    }
    return;
  }

  fs.writeFileSync(OUTPUT_PATH, next, 'utf8');
}

if (require.main === module) {
  main();
}

module.exports = {
  STATUS_PREFIX,
  SOURCE_PATH,
  OUTPUT_PATH,
  buildCoreModule,
  getStatus,
};
